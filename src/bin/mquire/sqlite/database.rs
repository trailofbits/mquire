/// Query data, as returned by Database::query
#[derive(Debug, Serialize)]
pub struct QueryData {
    /// The column names in the order they appear in the SQL query
    pub column_order: Vec<String>,

    /// The list of rows generated by sqlite
    pub row_list: RowList,
}

/// A wrapper around the rusqlite database connection
pub struct Database {
    /// The rusqlite connection
    conn: Connection,

    /// A map of table names to registered table plugins
    table_plugin_map: HashMap<String, Arc<dyn TablePlugin>>,
}

impl Database {
    /// Creates a new in-memory database
    pub fn new() -> Result<Self> {
        let conn = Connection::open_in_memory().map_err(|_| Error::DatabaseCreationFailed)?;

        Ok(Self {
            conn,
            table_plugin_map: HashMap::new(),
        })
    }

    /// Executes a query and returns the result
    pub fn query(&self, query: &str) -> Result<QueryData> {
        let mut stmt = self
            .conn
            .prepare(query)
            .map_err(|e| Error::InvalidSqlStatement(format!("{}", e)))?;

        let column_name_list: Vec<String> =
            stmt.column_names().iter().map(|s| s.to_string()).collect();

        let mapped_row_list = stmt
            .query_map([], |row| {
                let mut current_row = Row::new();

                for (column_index, column_name) in column_name_list.iter().enumerate() {
                    let rusqlite_value: Value = row.get(column_index)?;

                    let column_value = match rusqlite_value {
                        Value::Integer(i) => Some(ColumnValue::SignedInteger(i)),
                        Value::Real(f) => Some(ColumnValue::Double(f)),
                        Value::Text(s) => Some(ColumnValue::String(s)),
                        Value::Null => None,
                        Value::Blob(_) => {
                            return Err(rusqlite::Error::InvalidColumnType(
                                column_index,
                                column_name.clone(),
                                rusqlite::types::Type::Blob,
                            ));
                        }
                    };

                    current_row.insert(column_name.clone(), column_value);
                }

                Ok(current_row)
            })
            .map_err(|e| Error::TablePlugin(format!("{}", e)))?;

        let row_list = mapped_row_list
            .map(|row| row.map_err(|e| Error::TablePlugin(format!("{}", e))))
            .collect::<Result<RowList>>()?;

        Ok(QueryData {
            column_order: column_name_list,
            row_list,
        })
    }

    /// Registers a new table plugin
    pub fn register_table_plugin(&mut self, table_plugin: Arc<dyn TablePlugin>) -> Result<()> {
        if self.table_plugin_map.contains_key(&table_plugin.name()) {
            return Err(Error::DuplicatedTableName(table_plugin.name()));
        }

        let module = eponymous_only_module::<PluginVTab>();
        self.conn
            .create_module(
                table_plugin.name().as_str(),
                module,
                Some(table_plugin.clone()),
            )
            .map_err(|e| {
                Error::TablePluginRegistration(format!(
                    "Failed to register table {}: {}",
                    table_plugin.name(),
                    e
                ))
            })?;

        self.table_plugin_map
            .insert(table_plugin.name(), table_plugin);

        Ok(())
    }

    /// Returns a list of all registered table names
    pub fn get_table_names(&self) -> Vec<String> {
        let mut table_names: Vec<String> = self.table_plugin_map.keys().cloned().collect();
        table_names.sort();

        table_names
    }

    /// Returns the schema for a specific table
    pub fn get_table_schema(&self, table_name: &str) -> Option<BTreeMap<String, ColumnDef>> {
        self.table_plugin_map
            .get(table_name)
            .map(|plugin| plugin.schema())
    }

    /// Executes an SQL statement without returning results (for DDL statements like CREATE VIEW)
    pub fn execute(&self, sql: &str) -> Result<()> {
        self.conn
            .execute_batch(sql)
            .map_err(|e| Error::InvalidSqlStatement(format!("{}", e)))
    }

    /// Loads and executes SQL files from the autostart directory tree.
    ///
    /// The autostart directory is located at `$HOME/.config/trailofbits/mquire/autostart/`.
    /// Files are loaded from OS/architecture subdirectories in the following order:
    /// 1. `common/common/`: views for all platforms and architectures
    /// 2. `common/{arch}/`: views for all platforms on a specific architecture
    /// 3. `{os}/common/`: views for a specific platform on all architectures
    /// 4. `{os}/{arch}/`: views for a specific platform and architecture
    pub fn load_autostart_files(
        &self,
        os_type: crate::utils::OperatingSystemType,
        arch_type: crate::utils::ArchitectureType,
    ) {
        let home_dir = match std::env::var("HOME") {
            Ok(dir) => dir,
            Err(_) => return,
        };

        let base_dir = PathBuf::from(home_dir)
            .join(".config")
            .join("trailofbits")
            .join("mquire")
            .join("autostart");

        let os_str = os_type.to_string();
        let arch_str = arch_type.to_string();

        let dirs = [
            base_dir.join("common").join("common"),
            base_dir.join("common").join(&arch_str),
            base_dir.join(&os_str).join("common"),
            base_dir.join(&os_str).join(&arch_str),
        ];

        for dir in &dirs {
            self.load_sql_files_from(dir);
        }
    }

    /// Loads and executes all .sql files from a single directory, sorted alphabetically.
    fn load_sql_files_from(&self, dir: &Path) {
        if !dir.exists() {
            return;
        }

        let entries = match fs::read_dir(dir) {
            Ok(entries) => entries,
            Err(e) => {
                log::error!("Failed to read autostart directory {:?}: {}", dir, e);
                return;
            }
        };

        let mut sql_files: Vec<PathBuf> = entries
            .filter_map(|entry| entry.ok())
            .map(|entry| entry.path())
            .filter(|path| {
                path.is_file() && path.extension().and_then(|s| s.to_str()) == Some("sql")
            })
            .collect();

        sql_files.sort();

        for sql_file in sql_files {
            let sql_content = match fs::read_to_string(&sql_file) {
                Ok(content) => content,
                Err(e) => {
                    log::error!("Failed to read SQL file {:?}: {}", sql_file, e);
                    continue;
                }
            };

            if let Err(e) = self.execute(&sql_content) {
                log::error!("Failed to execute SQL file {:?}: {}", sql_file, e);
                continue;
            }
        }
    }
}

/// Virtual table implementation for table plugins
#[repr(C)]
struct PluginVTab<'vtab> {
    /// Base class. Must be first
    base: rusqlite::vtab::sqlite3_vtab,

    /// Reference to the table plugin
    plugin: Arc<dyn TablePlugin>,

    /// Column names in schema order
    column_names: Vec<String>,

    /// Columns that can be used as generation hints
    generator_inputs: Vec<String>,

    /// Phantom data for lifetime
    phantom: PhantomData<&'vtab ()>,
}

#[allow(unsafe_code)]
unsafe impl<'vtab> VTab<'vtab> for PluginVTab<'vtab> {
    type Aux = Arc<dyn TablePlugin>;
    type Cursor = PluginVTabCursor<'vtab>;

    fn connect(
        _db: &mut VTabConnection,
        aux: Option<&Self::Aux>,
        _args: &[&[u8]],
    ) -> rusqlite::Result<(String, Self)> {
        let plugin = aux
            .ok_or_else(|| rusqlite::Error::ModuleError("No plugin provided".to_string()))?
            .clone();

        let schema = plugin.schema();
        let generator_inputs = plugin.generator_inputs();

        let mut column_declarations = Vec::new();
        let mut column_names = Vec::new();

        for (column_name, column_def) in &schema {
            let sql_type = match column_def.column_type {
                ColumnType::SignedInteger => "INTEGER",
                ColumnType::String => "TEXT",
                ColumnType::Double => "REAL",
            };

            let hidden = match column_def.visibility {
                ColumnVisibility::Visible => "",
                ColumnVisibility::Hidden => " HIDDEN",
            };

            column_declarations.push(format!("{} {}{}", column_name, sql_type, hidden));
            column_names.push(column_name.clone());
        }

        let create_table_sql = format!(
            "CREATE TABLE {} ({})",
            plugin.name(),
            column_declarations.join(", ")
        );

        let vtab = Self {
            base: rusqlite::vtab::sqlite3_vtab::default(),
            plugin,
            column_names,
            generator_inputs,
            phantom: PhantomData,
        };

        Ok((create_table_sql, vtab))
    }

    fn best_index(&self, info: &mut IndexInfo) -> rusqlite::Result<()> {
        let referenced_generator_inputs: Vec<(usize, String)> = info
            .constraints()
            .enumerate()
            .filter_map(|(i, constraint)| {
                if constraint.is_usable()
                    && constraint.operator()
                        == rusqlite::vtab::IndexConstraintOp::SQLITE_INDEX_CONSTRAINT_EQ
                {
                    let col_idx = constraint.column() as usize;
                    if let Some(col_name) = self.column_names.get(col_idx)
                        && self.generator_inputs.contains(col_name)
                    {
                        return Some((i, col_name.clone()));
                    }
                }

                None
            })
            .collect();

        let mut used_generator_inputs = Vec::new();

        for (arg_index, (constraint_idx, col_name)) in (1..).zip(referenced_generator_inputs) {
            let mut usage = info.constraint_usage(constraint_idx);
            usage.set_argv_index(arg_index);
            usage.set_omit(true);

            used_generator_inputs.push(col_name);
        }

        if !used_generator_inputs.is_empty() {
            info.set_idx_str(&used_generator_inputs.join(","));
        }

        // Set estimated cost based on whether generator inputs are satisfied. This will
        // give a hint to SQLite to prefer query plans that pass constraints via JOINs
        let cost = if used_generator_inputs.is_empty() && !self.generator_inputs.is_empty() {
            f64::MAX
        } else {
            1.0
        };

        info.set_estimated_cost(cost);
        Ok(())
    }

    fn open(&'vtab mut self) -> rusqlite::Result<Self::Cursor> {
        Ok(PluginVTabCursor {
            base: rusqlite::vtab::sqlite3_vtab_cursor::default(),
            plugin: self.plugin.clone(),
            column_names: self.column_names.clone(),
            rows: Vec::new(),
            current_row: 0,
            phantom: PhantomData,
        })
    }
}

/// Virtual table cursor implementation
#[repr(C)]
struct PluginVTabCursor<'vtab> {
    /// Base class. Must be first
    base: rusqlite::vtab::sqlite3_vtab_cursor,

    /// Reference to the table plugin
    plugin: Arc<dyn TablePlugin>,

    /// Column names in schema order
    column_names: Vec<String>,

    /// Generated rows
    rows: RowList,

    /// Current row index
    current_row: usize,

    /// Phantom data for lifetime
    phantom: PhantomData<&'vtab PluginVTab<'vtab>>,
}

#[allow(unsafe_code)]
unsafe impl VTabCursor for PluginVTabCursor<'_> {
    fn filter(
        &mut self,
        _idx_num: c_int,
        idx_str: Option<&str>,
        args: &Filters<'_>,
    ) -> rusqlite::Result<()> {
        let used_generator_inputs: Vec<&str> =
            idx_str.map(|s| s.split(',').collect()).unwrap_or_default();

        let mut constraints = Constraints::new();
        for (column_name, value) in used_generator_inputs.iter().zip(args.iter()) {
            let column_value = match value {
                rusqlite::types::ValueRef::Integer(i) => ColumnValue::SignedInteger(i),
                rusqlite::types::ValueRef::Real(f) => ColumnValue::Double(f),
                rusqlite::types::ValueRef::Text(s) => {
                    ColumnValue::String(String::from_utf8_lossy(s).to_string())
                }
                rusqlite::types::ValueRef::Null => continue,
                rusqlite::types::ValueRef::Blob(_) => continue,
            };

            constraints.push(Constraint {
                column: column_name.to_string(),
                value: column_value,
            });
        }

        self.plugin
            .validate_constraints(&constraints)
            .map_err(|e| rusqlite::Error::ModuleError(format!("{:?}", e)))?;

        self.rows = self
            .plugin
            .generate(&constraints)
            .map_err(|e| rusqlite::Error::ModuleError(format!("{:?}", e)))?;

        self.current_row = 0;
        Ok(())
    }

    fn next(&mut self) -> rusqlite::Result<()> {
        self.current_row += 1;
        Ok(())
    }

    fn eof(&self) -> bool {
        self.current_row >= self.rows.len()
    }

    fn column(&self, ctx: &mut Context, col: c_int) -> rusqlite::Result<()> {
        let row = self
            .rows
            .get(self.current_row)
            .ok_or_else(|| rusqlite::Error::ModuleError("Invalid row index".to_string()))?;

        let column = row
            .iter()
            .nth(col as usize)
            .ok_or_else(|| rusqlite::Error::ModuleError("Invalid column index".to_string()))?;

        match &column.1 {
            None => ctx.set_result(&rusqlite::types::Null),
            Some(value) => match value {
                ColumnValue::SignedInteger(i) => ctx.set_result(i),
                ColumnValue::Double(f) => ctx.set_result(f),
                ColumnValue::String(s) => ctx.set_result(&s.as_str()),
            },
        }
    }

    fn rowid(&self) -> rusqlite::Result<i64> {
        Ok((self.current_row + 1) as i64)
    }
}

